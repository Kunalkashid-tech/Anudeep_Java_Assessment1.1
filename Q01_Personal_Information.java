// Java program to print name, hobbies and movie name.

package Anudeep_pro.java;

public class Q01_Personal_Information {

	public static void main(String[] args) {
		
		// Using string as data type to initialize name, hobbies and favorite movie name.
		String name = "Kunal Kashid";
		String hobbies = "Reading Books, Coding";
		String fav_movie_name = "Dangal";
		
		// Print name, hobbies and favorite movie name.
		System.out.println("Personal Information :-");
		System.out.println("Name is : "+name+"\nHobbies are : "+hobbies+"\nFavorite Movie is : "+fav_movie_name);

	}

}

/* 
  Output :-
Personal Information :-
Name is : Kunal Kashid
Hobbies are : Reading Books, Coding
Favorite Movie is : Dangal  
*/
