// Java program to display sum of float variables.

package Anudeep_pro.java;

public class Q04_Float_Variable_Sum {

	public static void main(String[] args) {
		
		// Initializing float variables.
		float num1 = 6.0f;
		float num2 = 3.0f;
		
		// Print sum of float variables.
		System.out.println("Sum of two float variables is : "+(num1 + num2));
		
	}

}


/*
  Output :-
  Sum of two float variables is : 9.0
*/