// Java program to display difference of double variables.

package Anudeep_pro.java;

public class Q05_Double_Variable_Difference {

	public static void main(String[] args) {
		
		// Initializing double variables.
		double num1 = 19.0;
		double num2 = 10.0;
		
		// Print sum of double variables.
		System.out.println("Difference between double variable is : "+(num1 - num2));
		
	}

}


/*
  Output :-
  Difference between double variable is : 9.0
*/